package day07_unary_operators;

public class ParkingLot {
    public static void main(String[] args) {
        int cars = 10;
        System.out.println(cars);


        cars = cars + 2; // 2 car come
        System.out.println(cars);

        cars = cars -5; // 5 cars left
        System.out.println(cars);

        cars = cars+ 2;
        System.out.println(cars);


    }
}
