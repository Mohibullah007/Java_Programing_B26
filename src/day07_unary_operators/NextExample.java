package day07_unary_operators;

public class NextExample {

    public static void main(String[] args) {
        char letter = 'G';
        System.out.println("Original " + letter++);
        System.out.println("Next four: ");
        System.out.println(letter++);
        System.out.println(letter++);
        System.out.println(letter++);
        System.out.println(letter++);

    }
}
