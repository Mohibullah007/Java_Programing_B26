package day08_relational_operators;

public class UpdateVariable {
    public static void main(String[] args) {
        int a = 10;
        a += 5;
        System.out.println(a);

        a -= 20;
        System.out.println(a);

        a *= 2;
        System.out.println(a);

        a /= -5;
        System.out.println(a);




    }
}
