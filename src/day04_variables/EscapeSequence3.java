package day04_variables;

public class EscapeSequence3 {

        public static void main(String[] args) {

            System.out.println("Hello Everyone \n\t My name is Mohibullah");
            System.out.println(" \n\n\n\n\t\"I live in Virginia\"");

            System.out.println("\\"); // this print me one back slash
            System.out.println("\\\\\\"); // this print me three back slash
            System.out.println("\'Hello to everyone\'"); // this print me one single code before and one after
        }
}
