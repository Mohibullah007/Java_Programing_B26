package day09_scanners;

public class LogicalPractice {
    public static void main(String[] args) {

        int num = 2;

        System.out.println(num > 5); // false
        System.out.println(num < 10); // true

        System.out.println(num >5 && num <10 ); // both have to match with the result.

        int n = 6;

        System.out.println(n > 5 && n < 10);

        int num2 = 9;

        System.out.println(num2 > 10 || num2 < 20); // one of the result have to match.







    }
}
